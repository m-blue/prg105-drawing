import turtle

t = turtle.Turtle()
d = turtle.Turtle()
w = turtle.Turtle()
r = turtle.Turtle()

def base(t):
    print(t)
    t.fd(100)
    t.lt(90)
    t.fd(100)
    t.lt(90)
    t.fd(100)
    t.lt(90)
    t.fd(100)
    t.up()
    t.fd(600)

def door(d):
    print(d)
    d.fd(33)
    d.lt(90)
    d.fd(45)
    d.rt(90)
    d.fd(20)
    d.rt(90)
    d.fd(45)
    d.rt(180)
    d.fd(45/2)
    d.up()
    d.lt(90)
    d.fd(5)
    d.down()
    d.fd(2)
    d.up()
    d.fd(600)

def window(w):
    w.fd(100)
    w.lt(90)
    w.fd(65)
    w.lt(90)
    w.up()
    w.fd(40)
    w.lt(90)
    w.down()
    w.fd(20)
    w.lt(90)
    w.fd(20)
    w.lt(90)
    w.fd(20)
    w.lt(90)
    w.fd(20)
    w.rt(180)
    w.fd(10)
    w.rt(90)
    w.fd(20)
    w.up()
    w.fd(600)

def roof(r):
    r.lt(90)
    r.fd(100)
    r.rt(50)
    r.fd(65)
    r.rt(80)
    r.fd(65)
    r.up()
    r.fd(600)

base(t)
window(w)
door(d)
roof(r)
turtle.done()
